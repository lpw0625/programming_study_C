// 헤더 파일 
// # include <stdio.h> 입력한 파일을 출력해주는데 도와주는 프로그램.

// main 함수 

#include <stdio.h>
// 전처리기 : 프로그램이 컴파일되기이전에 프로그램에 대한 사전 처리를 하는 과정입니다.

// main() : 프로그램의 진입점 역할을 수행하는 함수
void main()
{
	// ; (새미 콜론) : 한 문장의 끝을 알려주는 기호.
	printf("Hello World\n");

	// \n (개행 문자) : 해당하는 위치를 한 칸 아래로 내려주는 문자.
	printf("케인코는 뭉탱이코");

	// 전처리기는 컴파일러가 아닌 선행처리기에 의해서 처리되는 문자
	// 명령문 끝에 ";"을 사용하지 않는다.
}

